package com.classjava;



public class EvenOdd {
	public static void main(String[] args) {
		
		int i=0;
		
		String s="even";
		if(i % 2 == 0 && s=="even") {
			System.out.println(i);
			System.out.println(s);
		}
		else if
		(i % 2 == 0 && s== "odd") {
			System.out.println(i);
			System.out.println(s);
		}
		else {
			System.out.println("invalid");
		}
	
	}

}
